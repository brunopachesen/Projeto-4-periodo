    private ModeloBean retornaObjeto(){
        ModeloBean modelo = new ModeloBean();
        modelo.setNome(txModelo.getText().toUpperCase());
        modelo.setIdModelo(id);
        return modelo;
        
    }
    private boolean validaCampos(){
        boolean validador = true;
        txModelo.setText(txModelo.getText().trim());
        if(txModelo.getText().equals("")){
            validador = false;
            txModelo.setBorder(BorderFactory.createLineBorder(Color.red));
            lbMensagem.setText("*Campos obrigatorios n찾o preenchidos!");
        }else{
            txModelo.setBorder(MetalBorders.getTextFieldBorder());
        }
        return validador;
    }

